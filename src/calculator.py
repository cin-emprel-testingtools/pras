class Calculator:

    def addition(self, a, b):
        return a + b
    
    def addition3(self, a, b, c):
        return a + b + c
        
    def subtraction(self, a, b):
        return a - b
    
    def division(self, a, b):
        return a / b
    
    def difference(self, a, b):
        return a * b
    